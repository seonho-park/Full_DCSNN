    #                **kwargs)

class DCSNN_Full(nn.Module):
    def __init__(self, keypoint_gen, encoder_k, **kwargs):
        super(DCSNN_Full, self).__init__()
        encoder = keypoint_gen.base_model
        self.moco = MoCo(encoder, encoder_k, **kwargs)
        self.keypoint_gen = keypoint_gen

    def forward(self, im_q, im_k=None):
        kp_q = self.keypoint_gen(im_q)
        if im_k is not None:
            kp_k = self.keypoint_gen(im_k)
            moco_out = self.moco(im_q, im_k)
            return [kp_q, kp_k], moco_out
        else:
            moco_out = self.moco(im_q)
            return kp_q, moco_out


class MoCo(nn.Module):
    """
    https://arxiv.org/abs/1911.05722
    """
    def __init__(self, encoder_q, encoder_k, dim=128, K=65536, m=0.999, T=0.07, no_moco=False):
        super(MoCo, self).__init__()
        self.encoder_q = encoder_q
        
        self.dim = dim
        self.K = K
        self.m = m
        self.T = T
        self.activate = not no_moco

        if self.activate:
            self.encoder_k = encoder_k
            for param_q, param_k in zip(self.encoder_q.parameters(), self.encoder_k.parameters()):
                param_k.data.copy_(param_q.data)  # initialize
                param_k.requires_grad = False  # not update by gradient
        
        # # create the queue
        # self.register_buffer("queue", torch.randn(dim, K))
        # self.queue = nn.functional.normalize(self.queue, dim=0)
        # self.register_buffer("queue_ptr", torch.zeros(1, dtype=torch.long))

    @torch.no_grad()
    def _momentum_update_key_encoder(self):
        """
        Momentum update of the key encoder
        """
        for param_q, param_k in zip(self.encoder_q.parameters(), self.encoder_k.parameters()):
            param_k.data = param_k.data * self.m + param_q.data * (1. - self.m)

    def forward(self, im_q, im_k=None):
        q = self.encoder_q(im_q)  # queries: NxC
        if im_k is None:
            return q

        if self.activate:
            with torch.no_grad():  # no gradient to keys
                self._momentum_update_key_encoder()  # update the key encoder
                k = self.encoder_k(im_k)

            return q, k 
        else: # not moco
            return q, q